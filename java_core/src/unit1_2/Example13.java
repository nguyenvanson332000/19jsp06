package unit1_2;

// Link: https://www.w3schools.com/java/java_for_loop.asp
public class Example13 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        for (int i = 0; i <= 10; i = i + 2) {
            System.out.println(i);
        }
    }
}
